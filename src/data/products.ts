// Helper function to get similar products
export const getRelatedProducts = (productId: number, count: number = 4): Product[] => {
  const product = products.find(p => p.id === productId);
  if (!product) return [];
  
  // Get products in the same category, excluding the current product
  const sameCategory = products.filter(p => p.category === product.category && p.id !== product.id);
  
  // If we have enough products in the same category, return them
  if (sameCategory.length >= count) {
    return sameCategory.slice(0, count);
  }
  
  // Otherwise, add other products to meet the count
  const otherProducts = products.filter(p => p.id !== productId && !sameCategory.includes(p));
  return [...sameCategory, ...otherProducts].slice(0, count);
};

// Helper function to search products
export const searchProducts = (query: string): Product[] => {
  const searchTerm = query.toLowerCase().trim();
  if (!searchTerm) return products;
  
  return products.filter(product => 
    product.name.toLowerCase().includes(searchTerm) || 
    product.category.toLowerCase().includes(searchTerm)
  );
};

// Get products by category
export const getProductsByCategory = (category: string): Product[] => {
  if (!category || category === 'all') return products;
  return products.filter(product => product.category === category);
};

// Get product by ID
export const getProductById = (id: number): Product | undefined => {
  return products.find(product => product.id === id);
};